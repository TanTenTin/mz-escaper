//! 말투 프리셋과 system 프롬프트 조립.
//!
//! 이 챗봇의 유일한 "로직"이다. 사용자가 고른 말투를 지시문으로 바꾸고,
//! 그것을 system 메시지로 만들어 대화 맨 앞에 붙인다.

/// 프리셋 하나. `id`는 프런트가 보내는 값, `label`은 화면에 보이는 이름,
/// `instruction`은 모델에게 주는 실제 지시문이다.
pub struct Preset {
    pub id: &'static str,
    pub label: &'static str,
    pub instruction: &'static str,
}

/// 기본 제공 말투 목록. 여기에 항목을 추가하면 서버와 프런트 양쪽에 자동 반영된다
/// (프런트는 /api/presets 로 이 목록을 받아 버튼을 그린다).
pub const PRESETS: &[Preset] = &[
    Preset {
        id: "formal",
        label: "공적·격식체",
        // 상황을 '회사/직장'으로 못 박아 세분화한다. 격식만 입히는 게 아니라,
        // 직장에서 용인되지 않는 표현(비하·감정 분출)은 순화까지 한다. 이 프리셋은
        // '의미 그대로'가 아니라 '상황에 맞게 완곡화'가 목적이므로, 래퍼의 기본
        // 규칙(의미·문장종류 유지)을 이 지침이 의도적으로 덮어쓴다.
        instruction: "직장·회사에서 오가는 공적 커뮤니케이션 말투다. 상사나 동료에게 이메일·메신저로 전하듯 격식 있는 존댓말 문어체로 쓰고, 감탄사·이모지·줄임말·비속어·구어체를 쓰지 않으며 문장은 간결하고 명료하게 맺는다. 직장에서 용인되지 않는 표현은 그대로 옮기지 말고 순화한다. 상대의 지능·외모·인격을 깎아내리는 말은 사람이 아니라 사안·업무·결과물·방식에 대한 완곡하고 정중한 지적으로 바꾼다(예: \"멍청한데\" → \"이 부분은 접근 방식에 재고가 필요해 보입니다\"). 화·짜증·불평은 감정을 직접 드러내지 말고 정중한 문제 제기나 요청으로 눅인다. 다만 무엇을 전하려는지(우려·반대·요청 등)는 알아볼 수 있게 남긴다.",
    },
    // 나머지 말투는 하나씩 디테일하게 다듬어 다시 추가한다. 여기에 Preset 항목을
    // 넣으면 서버와 프런트(/api/presets)에 자동 반영된다.
];

/// 사용자가 직접 적는 말투 지시문의 최대 길이(문자 수).
/// 길이를 제한하는 이유는 두 가지다.
///   1) system 프롬프트가 비대해져 토큰을 낭비하는 것을 막는다.
///   2) 사용자가 긴 지시문으로 챗봇의 성격을 통째로 갈아치우는 것을 어느 정도 억제한다.
pub const MAX_CUSTOM_TONE_CHARS: usize = 200;

/// 프리셋 id로 지시문을 찾는다.
fn find_preset(id: &str) -> Option<&'static Preset> {
    PRESETS.iter().find(|p| p.id == id)
}

/// 말투 선택을 받아 최종 system 프롬프트를 만든다.
///
/// `tone_id`가 "custom"이면 `custom_tone`의 내용을 지시문으로 쓰고,
/// 그 외에는 프리셋에서 찾는다. 어느 쪽도 유효하지 않으면 Err.
pub fn build_system_prompt(tone_id: &str, custom_tone: Option<&str>) -> Result<String, String> {
    let instruction: String = if tone_id == "custom" {
        let raw = custom_tone.unwrap_or("").trim();

        if raw.is_empty() {
            return Err("직접 입력한 말투가 비어 있습니다.".to_string());
        }
        // chars().count() 로 세는 이유: 한글은 UTF-8에서 3바이트라 len()으로 재면
        // 실제 글자 수보다 훨씬 크게 나온다.
        if raw.chars().count() > MAX_CUSTOM_TONE_CHARS {
            return Err(format!(
                "말투 지시문은 {MAX_CUSTOM_TONE_CHARS}자 이내로 적어주세요."
            ));
        }
        // 줄바꿈을 공백으로 눌러 한 줄로 만든다. 여러 줄을 허용하면 사용자가
        // 가짜 대화 턴이나 가짜 지시 블록을 흉내 내 프롬프트 구조를 흔들기 쉬워진다.
        let flattened: String = raw
            .chars()
            .map(|c| if c == '\n' || c == '\r' { ' ' } else { c })
            .collect();

        format!("다음 말투: {flattened}")
    } else {
        find_preset(tone_id)
            .ok_or_else(|| format!("알 수 없는 말투입니다: {tone_id}"))?
            .instruction
            .to_string()
    };

    // 이 앱은 대화가 아니라 '말투 변환'이다. 그래서 프롬프트도 "답하라"가 아니라
    // "다시 쓰라"로 못을 박는다. 사용자가 준 문장의 뜻은 그대로 두고 말투만 바꾼다.
    // 입력이 질문이어도 그 질문에 답하지 않고, 질문 자체를 목표 말투로 옮겨 적는다.
    Ok(format!(
        "너는 한국어 문장의 말투를 바꿔 주는 변환기다. 사용자가 준 문장을 받아, \
         뜻은 그대로 유지한 채 아래의 말투로만 다시 쓴다.\n\n\
         [바꿀 말투]\n{instruction}\n\n\
         [규칙]\n\
         - 사용자의 문장에 '답하지' 않는다. 그 문장의 내용에 반응하거나, 질문에 대답하거나, \
         조언·설명·의견을 덧붙이지 않는다. 오직 주어진 문장을 목표 말투로 옮겨 적기만 한다.\n\
         - 말하려는 의도(전하려는 뜻·목적)는 살린다. 기본적으로는 원문의 의미와 문장 종류\
         (질문/평서/명령/감탄)를 유지하되, 위 말투 지침이 상황에 맞는 순화·완곡화·표현 조절을 \
         요구하면 그 지침을 우선한다.\n\
         - 변환 결과 문장만 출력한다. 어떻게 바꿨는지 설명하거나, 여러 후보를 나열하거나, \
         따옴표로 감싸지 않는다. 결과 텍스트 그 자체만 내보낸다."
    ))
}
